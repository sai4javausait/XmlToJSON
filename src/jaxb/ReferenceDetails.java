package jaxb;

public class ReferenceDetails {
	public String referenceType, referenceId, referenceLineId;

	public ReferenceDetails() {
		super();
	}

	public ReferenceDetails(String referenceType, String referenceId, String referenceLineId) {
		super();
		this.referenceType = referenceType;
		this.referenceId = referenceId;
		this.referenceLineId = referenceLineId;
	}
	
	
}
